def multiplication_table(num, up_to):
    """Prints the multiplication table for a number up to a given limit."""
    for i in range(1, up_to + 1):
        print(f"{num} x {i} = {num * i}")

# Example usage:
multiplication_table(5,10)  # Print the multiplication table of 5 up to 10
